package P7;

/**
 * 员工
 *
 * @author llq
 * @date 2022/11/28
 */
public class Employee {
    //定义员工类
        private String name;
        private int age;

        public Employee() {
            super();
        }

        public Employee(String name, int age) {
            super();
            this.name = name;
            this.age = age;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }
}




