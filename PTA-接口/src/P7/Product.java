package P7;

public class Product {
    String name;
    boolean isDefect;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean getIsDefect() {
        return isDefect;
    }

    public void setIsDefect(boolean defect) {
        isDefect = defect;
    }
}
